void LedsTask(void *argument)
{
  uint16_t leds = 0x8000;

  /* Infinite loop */
  for(;;)
  {
	HAL_SPI_Transmit(&hspi3, (uint8_t *)&leds, 1, 1);
	leds >>= 1;
	if (leds == 0)
	  leds = 0x8000;
	osDelay(100);
  }
}

void ServoTask(void *argument)
{
  /* Infinite loop */
  for(;;)
  {
    int i = 1000;

    for (; i <= 2000; i += 50)
    {
      htim2.Instance->CCR1 = i;
      osDelay(100);
    }

    for (; i >= 1000; i -= 50)
    {
      htim2.Instance->CCR1 = i;
      osDelay(100);
    }
  }
}
